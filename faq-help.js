$(document).ready(function() {
  $("#help_toggle").click(function() {
    var elem = $("#help_toggle").text();
    if (elem == "+") {
      //Stuff to do when btn is in the read more state
      $("#help_toggle").text("-");
      $("#help_more").slideDown();
    } else {
      //Stuff to do when btn is in the read less state
      $("#help_toggle").text("+");
      $("#help_more").slideUp();
    }
  });
});


